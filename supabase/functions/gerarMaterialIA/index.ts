
import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const openAIApiKey = Deno.env.get('OPENAI_API_KEY');

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    console.log('üöÄ Starting material generation...');
    const { materialType, formData } = await req.json();
    console.log('üìù Material type:', materialType);
    console.log('üìã Form data:', formData);

    let prompt = '';
    let responseFormat = '';

    switch (materialType) {
      case 'plano-de-aula':
        prompt = createLessonPlanPrompt(formData);
        responseFormat = getLessonPlanResponseFormat();
        break;

      case 'slides':
        prompt = createSlidesPrompt(formData);
        responseFormat = getSlidesResponseFormat();
        break;

      case 'atividade':
        prompt = createActivityPrompt(formData);
        responseFormat = getActivityResponseFormat();
        break;

      case 'avaliacao':
        prompt = createAssessmentPrompt(formData);
        responseFormat = getAssessmentResponseFormat();
        break;

      default:
        throw new Error(`Tipo de material n√£o suportado: ${materialType}`);
    }

    console.log('ü§ñ Calling OpenAI API...');
    const response = await fetch('https://api.openai.com/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${openAIApiKey}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'gpt-4o-mini',
        messages: [
          {
            role: 'system',
            content: 'Voc√™ √© um assistente especializado em educa√ß√£o brasileira, com conhecimento profundo da BNCC (Base Nacional Comum Curricular). Crie conte√∫do educacional de alta qualidade, contextualizado para a realidade brasileira.'
          },
          {
            role: 'user',
            content: `${prompt}\n\nFormato de resposta esperado:\n${responseFormat}`
          }
        ],
        temperature: 0.7,
        max_tokens: 4000
      }),
    });

    if (!response.ok) {
      const errorData = await response.text();
      console.error('‚ùå OpenAI API error:', errorData);
      throw new Error(`OpenAI API error: ${response.status} - ${errorData}`);
    }

    const data = await response.json();
    const generatedContent = data.choices[0].message.content;

    console.log('‚úÖ Content generated successfully');
    console.log('üìÑ Generated content length:', generatedContent.length);

    let parsedContent;
    try {
      parsedContent = JSON.parse(generatedContent);
      console.log('‚úÖ Content parsed successfully');
    } catch (parseError) {
      console.error('‚ùå JSON parsing error:', parseError);
      console.error('üîç Raw content:', generatedContent);
      throw new Error('Erro ao processar resposta da IA');
    }

    // Post-process the content based on material type
    if (materialType === 'plano-de-aula') {
      parsedContent = postProcessLessonPlan(parsedContent);
    }

    return new Response(JSON.stringify({
      success: true,
      content: parsedContent
    }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });

  } catch (error) {
    console.error('‚ùå Error in gerarMaterialIA function:', error);
    return new Response(JSON.stringify({
      success: false,
      error: error.message || 'Erro interno do servidor'
    }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});

